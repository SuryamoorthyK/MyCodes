package week1.day1;

public class OddNumber {

	public static void main(String args[])   
	{  
	int number=20;  
	System.out.println("List of odd numbers from 1 to 20: ");  
	for (int i=1; i<=number; i++)   
	{  
	if (i%2==1)   
	{  
	System.out.println(i + " ");  
	}  
	}  
	}  
	}  